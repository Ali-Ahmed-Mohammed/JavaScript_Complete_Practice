// Todo: Numbers: numbers can be written with or without decimals

let length = 16;
let weight = 7.5;

// Todo: String: are written with quotes. you can use single or double quotes

let color = "Yellow";
let lastName = 'Ali';

// Todo: Booleans: can only have two values true or false

let isTrue = true;
let isFalse = false;

// Todo: Object: are written with curly braces and aslo written as name:value pairs seperated by comma

const person = {
  firstName: 'Ali',
  lastName: 'Ahmed'
}

// Todo: Array: are written with square brackets and seperated by comma

const cars = ['saab', 'volvo', 'BMW'];

// Todo: Date

const date = new Date('2023-09-19');

// Todo: undefined: a variable without value, has the value of undefined

let h = undefined;
let j;   // also undefined

// Todo: Empty value: has nothing to do with undefined. an empty string has both legal value and a type

let emptyValue = ''; 


// Todo: the Typeof Operator: you can use to find the type of a javaScript variable.

typeof ""      // string
typeof 'Ali'  //  string
typeof 3     //   Number
typeof 3.5  //   Number


